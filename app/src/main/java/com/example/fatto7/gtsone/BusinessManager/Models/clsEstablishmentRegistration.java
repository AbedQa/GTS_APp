package com.example.fatto7.gtsone.BusinessManager.Models;

public class clsEstablishmentRegistration {
    private String id  = "";
    private String Name = "";
    private String ShortNameString = "";
    private String Address= "";

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getShortNameString() {
        return ShortNameString;
    }

    public void setShortNameString(String shortNameString) {
        ShortNameString = shortNameString;
    }

    public String getAddress() {
        return Address;
    }

    public void setAddress(String address) {
        Address = address;
    }





}
